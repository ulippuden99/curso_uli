let i = 1; // Declaramos la variable
console.log("Esto es un bucle for") // Imprimimos esto para saber que bucle se esta ejecutando
for (i; i <= 10; i++) {  // Colocamos el bucle for con la sintaxis (variable,condicion,incremento)
    console.log(i) // Mostramos el valor de i
}

console.log("Esto es un bucle while") // Imprimimos esto para saber que bucle se esta ejecutando
let a = 0; // Declaramos la variable
while (a < 10) { // Colocamos la condicion
    a++ // Aplicamos el incremento
    console.log(a) // Mostramos el valor de a
}

